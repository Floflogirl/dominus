FromNetToSerialCB::FromNetToSerialCB(SerialPort *serial, zmq::socket_t *socket):
    ReactorCallback(socket),
    _serial(serial),
    _socket(socket)
{
}

//==============================================================================
//
// Public methods
//
//==============================================================================
//------------------------------------------------------------------------------
// cb
//------------------------------------------------------------------------------
void FromNetToSerialCB::cb()
{
    std::cout << "MSG RECV!!!!!" << std::endl;

    // recv msg
    zmq::message_t zmsg;
    _socket->recv(&zmsg);
}
